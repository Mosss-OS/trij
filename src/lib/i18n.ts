export function useI18n() {
  const language = useSettingsStore((s) => s.language) as Language;
  const t = (key: keyof (typeof translations)["en-US"]) => {
    const langTranslations = translations[language] || translations["en-US"];
    return (langTranslations as Record<string, string>)[key] || translations["en-US"][key] || key;
  };
  return { t, language };
}
